/*
** A closure is a function defined inside another function and has access to variables that are declared and defined in 
** parent function.
** Closure has access to three variables:
** 1. declared in its own scope.
** 2. declared in parents scope.
** 3. declared global namespace.

** A closure is the combination of a function and the lexical environment within which that function was declared. 
*/
function makeAdder(x) {
  return function(y) {
    return x + y;
  };
}

var add5 = makeAdder(5);
var add10 = makeAdder(10);

console.log(add5(2));  // 7
console.log(add10(2)); // 12

/*
It is possible to emulate private methods using closures.
The following code illustrates how to use closures to define public functions that can access private functions and variables.
*/
var counter = (function() {
  var privateCounter = 0;
  function changeBy(val) {
    privateCounter += val;
  }
  return {
    increment: function() {
      changeBy(1);
    },
    decrement: function() {
      changeBy(-1);
    },
    value: function() {
      return privateCounter;
    }
  };   
})();

console.log(counter.value()); // logs 0
counter.increment();
counter.increment();
console.log(counter.value()); // logs 2
counter.decrement();
console.log(counter.value()); // logs 1

/*
we create a single lexical environment that is shared by three functions: counter.increment, counter.decrement, and counter.value.
three public functions are closures that share the same environment

Two different counter independence
Each closure references a different version of the privateCounter variable through its own closure
*/

var makeCounter = function() {
  var privateCounter = 0;
  function changeBy(val) {
    privateCounter += val;
  }
  return {
    increment: function() {
      changeBy(1);
    },
    decrement: function() {
      changeBy(-1);
    },
    value: function() {
      return privateCounter;
    }
  }  
};

var counter1 = makeCounter();
var counter2 = makeCounter();
alert(counter1.value()); /* Alerts 0 */
counter1.increment();
counter1.increment();
alert(counter1.value()); /* Alerts 2 */
counter1.decrement();
alert(counter1.value()); /* Alerts 1 */
alert(counter2.value()); /* Alerts 0 */



